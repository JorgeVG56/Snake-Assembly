;MACROS DE PROPOSITO GENERAL
SAVE_REGS MACRO REGS
    IRP R, <REGS>
        PUSH R
    ENDM
ENDM

RESTORE_REGS MACRO REGS
    IRP R, <REGS>
        POP R
    ENDM
ENDM

INIT_DS MACRO
    MOV AX,@DATA
    MOV DS,AX
ENDM

ONEB_TWOB_REG MACRO PARTE_BAJA, PARTE_ALTA, DATO
    MOV PARTE_ALTA, 0
    MOV PARTE_BAJA, DATO
ENDM

END_PROGRAM MACRO ERROR_CODE
    MOV AL, ERROR_CODE
    MOV AH, 4CH
    INT 21H 
ENDM

NEW_LINE MACRO
    PRINT_CAR 10
    PRINT_CAR 13
ENDM

PRINT MACRO CAD
    MOV AH, 9
    LEA DX, CAD
    INT 21H
ENDM

PRINTLN MACRO CAD
    PRINT CAD
    NEW_LINE
ENDM

LEE_CAR MACRO
    MOV AH, 1
    INT 21H ;RESULTADO EN AL
ENDM

PRINT_CAR MACRO DATO
    MOV DL, DATO
    MOV AH, 2
    INT 21H
ENDM

PRINT_NUM MACRO DATO
    MOV DL, DATO
    OR DL, 30H
    PRINT_CAR DL
ENDM
