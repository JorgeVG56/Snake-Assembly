    MOV OLD_COLOR, AH
    MOV OLD_PAGE, BH

    ; CAMBIAR A NUEVO MODO DE VIDEO
    MOV AH, 0
    MOV AL, 3
    INT 10H
    MOV AH, 1
    MOV CH, 32
    MOV CL, 0
    INT 10H
    
    CALL PAUSE_LOOP
    CALL DRAW_BG
    CALL INIT_SNAKE
    CALL GAME_LOOP         
    
    END_PROGRAM 0
    
MAIN ENDP

PAUSE_LOOP PROC
    CALL IMPRIMIR_MENU
PAUSAR:
    MOV AH, 01H
    INT 16H
    JZ PAUSAR ; ZF=1 -> no hay tecla
    MOV AH, 00H ;LIMPIAR BUFFER
    INT 16H
    CALL DRAW_BG
    RET
PAUSE_LOOP ENDP

GAME_LOOP PROC
.LOOP:
    ; LEER TECLADO
    MOV AH, 01H
    INT 16H
    JZ  .NO_KEY

    MOV AH, 0
    INT 16H
    CMP AH, 48H             ; ARRIBA
    JE .UP
    
    CMP AH, 4DH             ; DERECHA
    JE .RIGHT
    
    CMP AH, 50H             ; ABAJO
    JE .DOWN
    
    CMP AH, 4BH             ; IZQUIERDA
    JE .LEFT
    
    CMP AL, 1BH; ESC ?
    JE .PAUSE
    
    JMP .NO_KEY
.UP:
    MOV SNAKE_DIRECTION, 0
    JMP .NO_KEY
.RIGHT:
    MOV SNAKE_DIRECTION, 1
    JMP .NO_KEY
.DOWN:
    MOV SNAKE_DIRECTION, 2
    JMP .NO_KEY
.LEFT:
    MOV SNAKE_DIRECTION, 3
    JMP .NO_KEY
    
.PAUSE:
    CALL PAUSE_LOOP
    
.NO_KEY:
    CALL MOVE_SNAKE
    CALL DRAW_SNAKE

    CALL DELAY
    JMP .LOOP
.FIN:
    RET
GAME_LOOP ENDP

DELAY PROC
    SAVE_REGS< CX >
    MOV CX, ACT_ESPERA_MS
    ESPERA:
        SAVE_REGS< CX >
        MOV CX, CICLOS_MS
        DORMIR: 
    NOP
        LOOP DORMIR
        RESTORE_REGS< CX >
    LOOP ESPERA
    RESTORE_REGS< CX >
    RET
DELAY ENDP

