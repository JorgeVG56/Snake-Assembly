TITLE SNAKE GAME
INCLUDE MACROS.ASM
.MODEL SMALL
.STACK 100H
.DATA
    TABLERO         DB 25 DUP(80 DUP(?))
    PORTADA         DB "SNAKE"
    OLD_MODEL       DB 0
    OLD_COLOR       DB 0
    OLD_PAGE        DB 0
    INSIDE_COLOR    DB 00101111B
    BORDER_COLOR    DB 01100111B
    SERPIENTE       DW 500 DUP(?) 
.CODE
MAIN PROC
    INIT_DS
    
    ; OBTENER MODO DE VIDEO ACTUAL
    MOV AH,0FH
    INT 10H
    MOV OLD_MODEL,AL
    MOV OLD_COLOR,AH
    MOV OLD_PAGE,BH

    ; CAMBIAR A NUEVO MODO DE VIDEO
    MOV AH,0
    MOV AL,3
    INT 10H

    MOV AH,1
    MOV CH,1
    MOV CL,0
    INT 10H

    CALL DRAW_BORDER

    MOV CX,50
    FRAME:
        CALL DRAW_SNAKE
        LOOP FRAM

    END_PROGRAM 0
MAIN ENDP
DRAW_BORDER PROC
    SAVE_REGS <AX,BX,CX,DX>

    ; BORDER
    MOV AH,6
    MOV AL,0
    MOV BH,BORDER_COLOR
    MOV CH,0
    MOV CL,0
    MOV DH,24
    MOV DL,79
    INT 10H

    ; INSIDE
    MOV AH,7
    MOV AL,0
    MOV BH,INSIDE_COLOR
    MOV CH,1
    MOV CL,2
    MOV DH,23
    MOV DL,77
    INT 10H

    RESTORE_REGS <DX,CX,BX,AX>
    RET
DRAW_BORDER ENDP
DRAW_SNAKE PROC
    
DRAW_SNAKE ENDP

    END MAIN