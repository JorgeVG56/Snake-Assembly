TITLE SNAKE GAME
.MODEL SMALL
.STACK 100H
INCLUDE MACROS.ASM
.DATA
    TABLERO         DB 25 DUP(80 DUP(?))
    PORTADA         DB "SNAKE"
    OLD_MODEL       DB 0
    OLD_COLOR       DB 0
    OLD_PAGE        DB 0
    INSIDE_COLOR    DB 00101111B
    BORDER_COLOR    DB 01100111B

    ; SERPIENTE
    SERPIENTE       DW 500 DUP(?)
    COL_INICIAL     DB 20
    ROW_INICIAL     DB 10
    HEAD_IX         DW 0
    TAIL_IX         DW 0
    SNAKE_LEN       DB 3
    SNAKE_COLOR     DB 00000001B
    MAX_LEN         DW 500
    DIRECTION       DB 0
    ERASE_POS       DW 0
    
    ;POR SI CHOCA CON LOS BORDES
    MSJ_FIN DB "FIN DEL JUEGO $"
.CODE
MAIN PROC
    INIT_DS
    ; OBTENER MODO DE VIDEO ACTUAL
    MOV AH, 0FH
    INT 10H
    MOV OLD_MODEL, AL
    MOV OLD_COLOR, AH
    MOV OLD_PAGE, BH

    ; CAMBIAR A NUEVO MODO DE VIDEO
    MOV AH, 0
    MOV AL, 3
    INT 10H
    MOV AH, 1
    MOV CH, 32
    MOV CL, 0
    INT 10H
    
    CALL DRAW_BORDER
    CALL INIT_SNAKE
    CALL GAME_LOOP         
    
    END_PROGRAM 0
MAIN ENDP

GAME_LOOP PROC
.LOOP:
    ; LEER TECLADO
    MOV AH, 01H
    INT 16H
    JZ  .NO_KEY

    MOV AH, 0
    INT 16H
    CMP AH, 48H             ; ARRIBA
    JE .UP
    
    CMP AH, 4DH             ; DERECHA
    JE .RIGHT
    
    CMP AH, 50H             ; ABAJO
    JE .DOWN
    
    CMP AH, 4BH             ; IZQUIERDA
    JE .LEFT
    
    JMP .NO_KEY
.UP:
    MOV DIRECTION, 0
    JMP .NO_KEY
.RIGHT:
    MOV DIRECTION, 1
    JMP .NO_KEY
.DOWN:
    MOV DIRECTION, 2
    JMP .NO_KEY
.LEFT:
    MOV DIRECTION, 3
.NO_KEY:
    CALL MOVE_SNAKE
    CALL DRAW_SNAKE

    CALL DELAY
    JMP .LOOP
.FIN:
    RET
GAME_LOOP ENDP
DELAY PROC
MOV CX, 0FFFFh
.DELAY:
    NOP
    NOP
    NOP
    LOOP .DELAY

    RET
DELAY ENDP
INIT_SNAKE PROC
    SAVE_REGS <AX, BX, CX, DX>

    ; DIBUJAR COLA
    MOV AH, ROW_INICIAL
    MOV AL, COL_INICIAL
    SUB AL, 2               
    MOV SERPIENTE[0], AX
    MOV DH, AH
    MOV DL, AL
    MOV AL, 0DBh
    MOV BL, SNAKE_COLOR
    CALL DRAW_SEGMENT

    ; DIBUJAR SEGMENTO
    MOV AH, ROW_INICIAL     
    MOV AL, COL_INICIAL
    DEC AL                  
    MOV SERPIENTE[2], AX
    MOV DH, AH
    MOV DL, AL
    MOV AL, 0DBh
    MOV BL, SNAKE_COLOR
    CALL DRAW_SEGMENT

    ; DIBUJAR CABEZA
    MOV AH, ROW_INICIAL
    MOV AL, COL_INICIAL     
    MOV SERPIENTE[4], AX
    MOV DH, AH
    MOV DL, AL
    MOV AL, 0DBh
    MOV BL, SNAKE_COLOR
    CALL DRAW_SEGMENT

    MOV HEAD_IX, 2
    MOV TAIL_IX, 0
    MOV AX, SERPIENTE[0]
    MOV ERASE_POS, AX

    RESTORE_REGS <DX, CX, BX, AX>
    RET
INIT_SNAKE ENDP
DRAW_SEGMENT PROC
    SAVE_REGS <AX, BX, CX, DX>
    ; MOVER CURSOR
    MOV AH, 02H
    MOV BH, 0
    INT 10H

    ; IMPRIMIR
    MOV AH, 09H
    MOV BH, 0
    MOV CX, 1
    INT 10H

    RESTORE_REGS <DX, CX, BX, AX>
    RET
DRAW_SEGMENT ENDP
DRAW_BORDER PROC
    SAVE_REGS <AX,BX,CX,DX>

    ; BORDER
    MOV AH, 6
    MOV AL, 0
    MOV BH, BORDER_COLOR
    MOV CH, 0
    MOV CL, 0
    MOV DH, 24
    MOV DL, 79
    INT 10H

    ; INSIDE
    MOV AH, 7
    MOV AL, 0
    MOV BH, INSIDE_COLOR
    MOV CH, 1
    MOV CL, 2
    MOV DH, 23
    MOV DL, 77
    INT 10H

    RESTORE_REGS <DX,CX,BX,AX>
    RET
DRAW_BORDER ENDP
DRAW_SNAKE PROC
    SAVE_REGS <AX,BX,CX,DX,SI>

    ; DIBUJAR CABEZA
    MOV SI, HEAD_IX
    SHL SI, 1
    MOV AX, SERPIENTE[SI]
    MOV DH, AH              ; FILA
    MOV DL, AL              ; COLUMNA
    MOV AL, 0DBh            ; CARACTER
    MOV BL, SNAKE_COLOR     ; COLOR
    CALL DRAW_SEGMENT

    ; BORRAR COLA
    MOV AX, ERASE_POS
    MOV DH, AH
    MOV DL, AL
    MOV AL, ' '
    MOV BL, INSIDE_COLOR
    CALL DRAW_SEGMENT
    
    RESTORE_REGS <SI,DX,CX,BX,AX>
    RET
DRAW_SNAKE ENDP

MOVE_SNAKE PROC
    SAVE_REGS <AX,BX,CX,DX,SI>

    ; NUEVA POSICION DE LA CABEZA
    MOV SI, HEAD_IX
    SHL SI, 1
    MOV AX, SERPIENTE[SI]
    CMP DIRECTION, 0
    JNE .CHECK_RIGHT
    DEC AH
    JMP .NUEVA_POS

.CHECK_RIGHT:
    CMP DIRECTION, 1
    JNE .CHECK_DOWN
    INC AL
    JMP .NUEVA_POS
.CHECK_DOWN:

    CMP DIRECTION, 2
    JNE .CHECK_LEFT
    INC AH
    JMP .NUEVA_POS
.CHECK_LEFT:

    DEC AL
.NUEVA_POS:

    ;DECTAR ERRORES POR LOS BORDES
    CMP AH, 1       
    JL .CHOQUE
    CMP AH, 23       
    JG .CHOQUE
    CMP AL, 2       
    JL .CHOQUE
    CMP AL, 77       
    JG .CHOQUE
    
    ; INSERTAR NUEVA CABEZA
    MOV BX, HEAD_IX
    INC BX
    CMP BX, MAX_LEN
    JL  .OK_HEAD
    MOV BX, 0

.OK_HEAD:
    MOV HEAD_IX, BX
    MOV SI, BX
    SHL SI, 1
    MOV SERPIENTE[SI], AX

    ; GUARDAR POSICION VIEJA DE LA COLA
    MOV BX, TAIL_IX
    MOV SI, BX
    SHL SI, 1
    MOV AX, SERPIENTE[SI]
    MOV ERASE_POS, AX

    ; AVANZAR TAIL_IX
    INC BX
    CMP BX, MAX_LEN
    JL  .OK_TAIL
    MOV BX, 0
.OK_TAIL:
    MOV TAIL_IX, BX

    RESTORE_REGS <SI,DX,CX,BX,AX>
    RET
    
;ACCION CHOCAR
.CHOQUE:
    MOV AH, 02H
    MOV BH, 0
    MOV DH, 12       
    MOV DL, 30       
    INT 10H

    MOV AH, 09H
    LEA DX, MSJ_FIN
    INT 21H

    MOV AH, 4CH
    INT 21H
MOVE_SNAKE ENDP

    END MAIN